<?php

namespace protocol\http;

use Exception;
use PHPUnit\Framework\TestCase;
use Surreal\SurrealHTTP;

class AuthTest extends TestCase
{
    private static SurrealHTTP $db;

    /**
     * @throws Exception
     */
    public static function setUpBeforeClass(): void
    {
        self::$db = new SurrealHTTP(
            host: "http://localhost:8000",
            target: ["namespace" => "test", "database" => "test"]
        );

        $token = self::$db->signin([
            "root" => "root",
            "root" => "root"
        ]);

        self::$db->setToken($token);

        parent::setUpBeforeClass(); // TODO: Change the autogenerated stub
    }

    /**
     * @throws Exception
     */
    public function testSignup(): void
    {
        $token = self::$db->signup([
            "user" => "beau",
            "pass" => "123456",
            "ns" => "test",
            "db" => "test"
        ]);

        self::assertIsString($token);
    }

    /**
     * @throws Exception
     */
    public function testSignIn(): void
    {
        try {
            $token = self::$db->signin([
                "user" => "beau",
                "pass" => "123456",
                "ns" => "test",
                "db" => "test"
            ]);

            $this->assertIsString($token);
        } catch (Exception $e) {
            $this->assertTrue(true);
        }
    }

    public function testInvalidate(): void
    {
        self::$db->invalidate();
        $this->assertNull(self::$db->getToken());
    }

    public static function tearDownAfterClass(): void
    {
        self::$db->close();
        parent::tearDownAfterClass(); // TODO: Change the autogenerated stub
    }
}