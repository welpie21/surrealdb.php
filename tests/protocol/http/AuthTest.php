<?php

namespace protocol\http;

use Exception;
use PHPUnit\Framework\TestCase;
use Surreal\Core\Client\SurrealHTTP;

class AuthTest extends TestCase
{
    private static SurrealHTTP $db;

    /**
     * @throws Exception
     */
    public static function setUpBeforeClass(): void
    {
        self::$db = new SurrealHTTP(
            host: "http://localhost:8000",
            target: ["namespace" => "test", "database" => "test"]
        );

        parent::setUpBeforeClass(); // TODO: Change the autogenerated stub
    }

    /**
     * @throws Exception
     */
    public function testScopeAuth(): void
    {
        $token = self::$db->signup([
            "email" => "beau.one",
            "pass" => "beau.one",
            "ns" => "test",
            "db" => "test",
            "sc" => "account"
        ]);

        $this->assertIsString($token);
        $this->assertNotEmpty($token);

        $token = self::$db->signin([
            "email" => "beau.one",
            "pass" => "beau.one",
            "ns" => "test",
            "db" => "test",
            "sc" => "account"
        ]);

        $this->assertIsString($token);
        $this->assertNotEmpty($token);
    }

    /**
     * @throws Exception
     */
    public function testSigninRoot(): void
    {
        $token = self::$db->signin([
            "user" => "root",
            "pass" => "root"
        ]);

        $this->assertIsString($token);
    }

    public function testSigninNamespace(): void
    {
        $token = self::$db->signin([
            "user" => "julian",
            "pass" => "123!",
            "ns" => "test"
        ]);

        $this->assertIsString($token);
    }

    /**
     *
     * @throws Exception
     */
    public function testSigninDatabase(): void
    {
        $token = self::$db->signin([
            "user" => "beau",
            "pass" => "123!",
            "ns" => "test",
            "db" => "test"
        ]);

        $this->assertIsString($token);
    }


    public function testInvalidate(): void
    {
        $this->assertNull(self::$db->auth->getToken());
    }

    /**
     * @throws Exception
     */
    public static function tearDownAfterClass(): void
    {
        self::$db->close();
        parent::tearDownAfterClass(); // TODO: Change the autogenerated stub
    }
}