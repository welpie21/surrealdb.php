<?php

namespace protocol\http;

use PHPUnit\Framework\TestCase;
use Surreal\SurrealHTTP;

class QueryTest extends TestCase
{
    private static SurrealHTTP $db;

    public static function setUpBeforeClass(): void
    {
        self::$db = new SurrealHTTP(
            host: "http://localhost:8000",
            target: ["namespace" => "test", "database" => "test"]
        );

        $token = self::$db->signin([
            "user" => "beaurt",
            "pass" => "123456"
        ]);

        self::assertIsString($token, "Token is not a string");

        self::$db->setToken($token);

        parent::setUpBeforeClass(); // TODO: Change the autogenerated stub
    }

    /**
     * @throws Exception
     */
    public function testCreation(): void
    {
        $response = self::$db->create("test", [
            "surreal" => "is",
            "the" => "best",
            "database" => [
                "ever" => "created"
            ],
            "we-are" => 1
        ]);

        $this->assertIsObject($response);
    }

    /**
     * @throws Exception
     */
    public function testUpdate(): void
    {
        $response = self::$db->update("person:beau", [
            "age" => 26
        ]);

        $this->assertIsObject($response);
    }

    /**
     * @throws Exception
     */
    public function testMerge(): void
    {
        $response = self::$db->merge("person:beau", [
            "name" => "Beau",
            "age" => 30
        ]);

        $this->assertIsObject($response);
    }

    /**
     * @throws Exception
     */
    public function testDelete(): void
    {
        $response = self::$db->delete("person:julian");
        $this->assertIsObject($response);
    }

    /**
     * @throws Exception
     */
    public function testSQL(): void
    {
        $response = self::$db->sql("SELECT * FROM person WHERE age >= 18");
        $this->assertIsArray($response);
    }

    public static function tearDownAfterClass(): void
    {
        self::$db->close();
        parent::tearDownAfterClass();
    }
}